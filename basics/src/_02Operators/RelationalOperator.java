package _02Operators;

public class RelationalOperator {
public static void main(String[] args) {
  // relational operators

  // equality
  int a = 23;
  double b = 23.000;

  boolean result = a == b;
  System.out.println(result+"\n");

  // difference
  result = a != b;
  System.out.println(result + "\n");


  // greater than
  result = a < b;
  System.out.println(result + "\n");


  // less than
  result = a > b;
  System.out.println(result + "\n");



  // less than equal
  result = a >= b;
  System.out.println(result + "\n");



  // greater than equal
  result = a <= b;
  System.out.println(result + "\n");
}
}



