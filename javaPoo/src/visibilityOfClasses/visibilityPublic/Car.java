package visibilityOfClasses.visibilityPublic;

public class Car {
  public void manufacturingDate() {
    System.out.println("Fabricado: 11/2013");
  }

  public void release() {
    System.out.println("7/2014");
  }

  public void modelAndManufacturer() {
    System.out.println("Modelo: Voyage");
    System.out.println("Fabricante: Volkswagen");
  }

  public void motor() {
    System.out.println("Motor da familia: VHT / 1.6 8V G6");
  }

  public void numberOfPorts() {
    System.out.println("Possue: 4 portas");
  }

  public void carColor() {
    System.out.println("Cor do carro: Branco");
  }
}
