package operators;

public class AssignmentOperator {

  public static void main(String[] args) {
    // assignment operator

    String assignment = "variable";
    assignment = "overwrite";
    System.out.println(assignment);

    char initialCharacter = 'F';
    initialCharacter = 'M';
    System.out.println(initialCharacter);

    boolean userOnline = false;
    userOnline = true;
    System.out.println(userOnline);

  }
}
